import Foundation

extension Data {
    
    func toBase64URL() -> String {
        var result = self.base64EncodedString()
        
        result = result.replacingOccurrences(of: "+", with: "-")
        result = result.replacingOccurrences(of: "/", with: "_")
        result = result.replacingOccurrences(of: "=", with: "")
        return result
    }
}
